principal = 1000
#Value 1000 represents $1000

rate = 0.05
#Value 0.05 represents 5% annual interest

time = 3
#Value 3 represents 3 years

#Calculating the simple interest using the 'I = P * R * T' formula,
# where;
#  'I' represents the interest
#  'P' represents the principal
#  'R' represents the rate
#  'T' represents the time
interest = principal * rate * time
print("The simple interest is:", interest)  
